                <h3 class="title_blue">旅游资源</h3>
                <p>阳春旅游资源丰富，文化古迹众多，有“广东小桂林”之称。</p>
                <h4 class="title_blue">凌霄岩</h4>
                <p>凌霄岩是以岩溶地貌为主的地文景观，它具有中国大陆架南端典型的喀斯特地貌特征。
                凌霄岩洞内的巨大石柱、石笋大多属典型的棕榈片造型，也称“石灵芝”造型，被称为“世界石灵芝王国”。</p>
                <h4 class="title_blue">花滩森林公园</h4>
                <p>花滩森林公园是一九九三年广东省林业厅批准建设的省级森林公园，位于广东阳春市境内中心的西山山脉地带，属亚热带气候，公园境内面积4000多公顷，水面面积400多公顷。 </p>
                <h4 class="title_blue">慈云岩与通天蜡烛</h4>
                <p>慈云岩与通天蜡烛在春湾中山公园中，隔湖相望。该岩是阳春著名的古迹之一。</p>
                <h4 class="title_blue">仙湖</h4>
                <p>仙湖与白水瀑布一样，位于八甲山区。该湖远名仙家垌水库，总面积16.6平方公里，集雨面积200平方公里，最宽处1900米，水最深处达40米。</p>
                <h4 class="title_blue">白水瀑布</h4>
                <p>白水瀑布位于阳春市八甲镇10多公里外的崇山峻岭中。瀑布垂直落差达255米，被誉为“岭南第一瀑”</p>
                <h4 class="title_blue">龙宫岩</h4>
                <p>岩洞全长1400米，龙泉溪水清澈，又称伏旧岩，国家二级景点。</p>
                <h4 class="title_blue">通真岩</h4>
                <p>通真岩，原名铜石岩，位于春湾圩东南方约1公里的铜石山上，是古代铜石寺的遗址。</p>
                <h4 class="title_blue">鹅凰嶂</h4>
                <p>鹅凰嶂自然保护区是森林生态系统类型保护区，主要保护对象是华南地区热带北缘季雨林和山地雨林为主体的森林生态系统。</p>
                <h4 class="title_blue">玉溪仙景</h4>
                <p>一洞顶的“皇冠”石，酷似皇冠，该洞以“金盏叠泉”最为壮观，一个个金黄色的石盏满载清澈的泉水往下流。</p>
                <h4 class="title_blue">古雅崆峒</h4>
                <p>崆峒岩位于阳春城西3公里处，始建于明代万历丁丑年间，清乾隆二十一年阳春县令姜山重修寺宇。崆峒岩又名“四崆峒山”。</p>
                <h4 class="title_blue">鱼王石</h4>
                <p>在春城镇北面8公里的漠阳江畔，有两尊奇石。从东面望去左石象鳌鱼，右石象黄鱼。</p>
                <h4 class="title_blue">春湾石林</h4>
                <p>春湾石林位于春湾镇东北2公里的蓬花垌。珠江电影制片厂、香港亚洲电视台等在这里先后拍摄了《岳飞》、《天蚕变》等30多部影视片。</p>
